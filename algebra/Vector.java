
public class Vector {

	private double x;
	private double y;
	private double z;

	public Vector(double theX, double theY, double theZ) {
         x = theX;
	 y = theY;
	 z = theZ;
     }

     public double getXValue() {
	     return x; 
     }

     public double getYValue() {
	     return y; 
     }

    public double getZValue() {
	     return z; 
     }
}








